//-----------------------------------------------------------------------------
// Test는 num개의 int를 free-store에 확보한 contiguous 메모리에 저장한다.
//-----------------------------------------------------------------------------

class Test {
public:
	Test( ) = default;

	// 저장된 int의 값을 앞에서 부터 7개만 출력
	void show( ) const {						
		cout << num << " - ";
		for ( int i = 0; i < 7; ++i )
			cout << p[ i ] << " ";
		cout << endl;
	}


private:
	size_t num {};				// free-store에 저장한 int의 개수	
	unique_ptr<int[]> p {};		// num개의 int가 저장되어 있는 contiguous 메모리를 관리하는 포인터

public:
	double average() const {
		if (num == 0) return 0.0;
		long long sum = accumulate(p.get(), p.get() + num, 0LL);
		return static_cast<double>(sum) / num;
	}
	bool operator<(const Test& other) const {
		return num < other.num;
	}
	void read(ifstream& in) {
		in.read(reinterpret_cast<char*>(&num), sizeof(int));
		p.release();
		p = make_unique<int[]>(num);
		in.read(reinterpret_cast<char*>(p.get()), sizeof(int) * num);
	}
	friend ostream& operator<<( ostream& os, const Test& t ) {
		os.write( (char*)&t.num, sizeof(int) );
		os.write( (char*)t.p.get(), sizeof(int) * t.num );
		return os;
	}
};

